# 2.1 Comenzamos con -> Decimal, Binario, Octal y Hexadecimal

# Sistema decimal
a = 10
print(f'a: decimal {a}')

# Sistema binario
a = 0b1010
print(f'a: binario {a}')

# Sistema octal (8)
a = 0o12
print(f'a: octal {a}')

# Sistema hexadecimal
a = 0xA
print(f'a: hexadecimal {a}')


# 2.2 Seguimos con -> Decimal, Binario, Octal y Hexadecimal. Ahora con int('valor', base)
# Base Decimal -> (10)
a = int('23', 10)
print(f'a: base decimal {a}')

# Base Binario -> (2)
a = int('10111', 2)
print(f'a: base binaria {a}')


